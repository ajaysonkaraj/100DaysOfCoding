import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        int i=0;
        Scanner sc = new Scanner(System.in);
        while(sc.hasNext())  //user inputs
        {
            i++;
            System.out.println(i+" "+sc.nextLine());
        }
        sc.close();
    }
}